export const ADMIN_LANGS = [
  { id: 'es', short: 'ES', title: 'Español' },
  { id: 'pt', short: 'PT', title: 'Português' },
];

export function getAdminLang() {
  const saved = localStorage.getItem(STORAGE_KEY);
  return STRINGS[saved] ? saved : 'es';
}

export function setAdminLang(lang) {
  if (!STRINGS[lang]) return;
  localStorage.setItem(STORAGE_KEY, lang);
  document.documentElement.lang = lang;
}

export function t(key, vars = {}) {
  const lang = getAdminLang();
  let text = STRINGS[lang]?.[key] ?? STRINGS.es[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    text = text.replace(`{${k}}`, String(v));
  }
  return text;
}

